class Point:
    
    def __init__(self, row, col):
        """
        =======================================================================
         Description: Constructor.
        =======================================================================
         Arguments:
        -----------------------------------------------------------------------
            1. row : int
            2. col : int
        =======================================================================
        """
        self.row = row
        self.col = col
        
    
    def __eq__(self, other):
        return self.row == other.row and self.col == other.col
    
    def __str__(self):
        return '({0},{1})'.format(self.row, self.col)
    
    
    @staticmethod
    def gen(tuple_row_col):
        """
        =======================================================================
         Description: Return Point with Tuple Row and Col parameters.
        =======================================================================
         Arguments:
        -----------------------------------------------------------------------
            1. tuple (int, int) : Tuple of Row and Col.
        =======================================================================
         Return: Point (with Tuple Row and Col parameters).
        =======================================================================
        """
        return Point(tuple_row_col[0], tuple_row_col[1])
    
    
    
